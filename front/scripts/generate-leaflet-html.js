/**
 * Generates lib/map/leafletHtml.ts containing a complete offline Leaflet map
 * with all tiles embedded as base64 data URIs.
 *
 * Run after download-tiles.js: node scripts/generate-leaflet-html.js
 */

const fs = require("fs");
const path = require("path");

const TILES_DIR = path.join(__dirname, "..", "assets", "map-tiles");
const OUTPUT = path.join(__dirname, "..", "lib", "map", "leafletHtml.ts");

// Collect all tiles as base64
const tileEntries = [];

const zoomDirs = fs.readdirSync(TILES_DIR).filter((d) =>
  fs.statSync(path.join(TILES_DIR, d)).isDirectory()
);

for (const z of zoomDirs) {
  const xDirs = fs.readdirSync(path.join(TILES_DIR, z)).filter((d) =>
    fs.statSync(path.join(TILES_DIR, z, d)).isDirectory()
  );
  for (const x of xDirs) {
    const yFiles = fs.readdirSync(path.join(TILES_DIR, z, x)).filter((f) =>
      f.endsWith(".png")
    );
    for (const yFile of yFiles) {
      const y = yFile.replace(".png", "");
      const filePath = path.join(TILES_DIR, z, x, yFile);
      const base64 = fs.readFileSync(filePath).toString("base64");
      tileEntries.push({ z, x, y, base64 });
    }
  }
}

console.log(`Encoding ${tileEntries.length} tiles as base64...`);

// Build the tile JS object entries
const tileMapLines = tileEntries.map(
  ({ z, x, y, base64 }) => `"${z}/${x}/${y}":"data:image/png;base64,${base64}"`
);

const html = `<!DOCTYPE html>
<html>
<head>
<meta name="viewport" content="width=device-width,initial-scale=1,maximum-scale=1,user-scalable=no"/>
<link rel="stylesheet" href="https://unpkg.com/leaflet@1.9.4/dist/leaflet.css" crossorigin=""/>
<style>
  *{margin:0;padding:0}
  html,body,#map{width:100%;height:100%}
</style>
<script>
// Inline Leaflet CSS for offline
</script>
</head>
<body>
<div id="map"></div>
<script src="https://unpkg.com/leaflet@1.9.4/dist/leaflet.js" crossorigin=""></script>
<script>
var TILES={${tileMapLines.join(",")}};

var map=L.map("map",{zoomControl:false}).setView([6.1714,-75.5901],15);

L.TileLayer.Offline=L.TileLayer.extend({
  getTileUrl:function(coords){
    var key=coords.z+"/"+coords.x+"/"+coords.y;
    return TILES[key]||"";
  }
});
new L.TileLayer.Offline("",{maxZoom:16,minZoom:13}).addTo(map);

var markers=[];
var polyline=null;

// Listen for messages from React Native
document.addEventListener("message",function(e){handleMsg(e);});
window.addEventListener("message",function(e){handleMsg(e);});

function handleMsg(e){
  try{
    var msg=JSON.parse(e.data);
    if(msg.type==="setMarker"){
      var m=L.marker([msg.lat,msg.lng],{
        icon:L.divIcon({className:"",html:'<div style="background:red;width:16px;height:16px;border-radius:50%;border:2px solid white;box-shadow:0 0 4px rgba(0,0,0,0.4)"></div>'})
      }).addTo(map);
      markers.push(m);
      if(msg.center) map.setView([msg.lat,msg.lng],15);
    }
    if(msg.type==="setPolyline"){
      if(polyline){map.removeLayer(polyline);}
      polyline=L.polyline(msg.points.map(function(p){return[p.latitude,p.longitude]}),{color:"#e00000",weight:4}).addTo(map);
      map.fitBounds(polyline.getBounds(),{padding:[40,40]});
    }
    if(msg.type==="clearMarkers"){
      markers.forEach(function(m){map.removeLayer(m)});
      markers=[];
      if(polyline){map.removeLayer(polyline);polyline=null;}
    }
    if(msg.type==="setView"){
      map.setView([msg.lat,msg.lng],msg.zoom||15);
    }
  }catch(err){console.log(err)}
}
</script>
</body>
</html>`;

// We need to inline Leaflet CSS + JS for full offline. Let's fetch them.
const https = require("https");

function fetchUrl(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      let data = "";
      res.on("data", (chunk) => (data += chunk));
      res.on("end", () => resolve(data));
    }).on("error", reject);
  });
}

async function main() {
  console.log("Fetching Leaflet CSS and JS for offline embedding...");
  const leafletCss = await fetchUrl("https://unpkg.com/leaflet@1.9.4/dist/leaflet.css");
  const leafletJs = await fetchUrl("https://unpkg.com/leaflet@1.9.4/dist/leaflet.js");

  const offlineHtml = html
    .replace(
      '<link rel="stylesheet" href="https://unpkg.com/leaflet@1.9.4/dist/leaflet.css" crossorigin=""/>',
      `<style>${leafletCss}</style>`
    )
    .replace(
      '<script src="https://unpkg.com/leaflet@1.9.4/dist/leaflet.js" crossorigin=""></script>',
      `<script>${leafletJs}</script>`
    );

  // Escape backticks and ${} in the HTML for template literal safety
  const escaped = offlineHtml.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

  const tsContent = `/**
 * Auto-generated offline Leaflet HTML with embedded tiles.
 * Generated by: node scripts/generate-leaflet-html.js
 * Do not edit manually.
 */
export const LEAFLET_HTML = \`${escaped}\`;
`;

  fs.writeFileSync(OUTPUT, tsContent);
  console.log(`Generated ${OUTPUT} (${(tsContent.length / 1024).toFixed(0)} KB)`);
}

main().catch(console.error);
